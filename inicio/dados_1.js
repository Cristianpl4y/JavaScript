// valor literal aplicação fica complicada de entender.
console.log("Caneta");
console.log(10); 
console.log(6.4);
console.log(1.5);
console.log(7.9);

// aplicação passando os valores para as variaveis com nomes sugestivos fica mais claro de entender.
let quantidade = 10;
let preco = 6.4;
let imposto = 1.5;
let precoFinal =  preco + imposto;

console.log("Caneta");
console.log(quantidade); 
console.log(preco);
console.log(imposto);
console.log(precoFinal);

